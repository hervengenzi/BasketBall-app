class Player{
    id: number
    name: string
    team: string
    salary: string
    careerPoints: number

    constructor(id: number, name: string, team: string, salary: string, careerPoints: number){
        this.id = id,
        this.name = name,
        this.team = team,
        this.salary = salary,
        this.careerPoints = careerPoints
    }
}